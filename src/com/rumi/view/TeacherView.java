     public void displayClsToComboBox() {

        cmbCls.addItem("Select Category");
        List<Classes> list = clsService.getList();
        for (Classes c : list) {
            cmbCls.addItem(c.getCls());
        }
    }

    

    public void displayDataIntoTable() {
        DefaultTableModel model = (DefaultTableModel) tbDisplay.getModel();
        model.setRowCount(0);
        Object[] row = new Object[8];
        List<Teacher> list = commonService.getList();
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getGender();
            row[3] = list.get(i).getFname();
            Classes cls= (Classes) clsService.get(list.get(i).getCls().getId());
            row[4] = cls.getCls();
            row[5] = list.get(i).getSubject();
            row[6] = list.get(i).getContract();
            row[7] = list.get(i).getAddress();
            model.addRow(row);
        }
    }

