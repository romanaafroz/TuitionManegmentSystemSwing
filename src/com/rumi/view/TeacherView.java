    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String gender = null;
        if (rMale.isSelected()) {
            gender = rMale.getText().trim();
        } else {
            gender = rFemale.getText().trim();
        }
        String cls = cmbCls.getSelectedItem().toString();
        Classes clses =  (Classes) clsService.getByName(cls);
        Teacher teacher = new Teacher(txtName.getText().trim(), gender, txtFname.getText().trim(), clses, cmbSubject.getSelectedItem().toString(), txtContract.getText().trim(), txtAddress.getText().trim());
        commonService.save(teacher);
        displayDataIntoTable();
        reset();
        JOptionPane.showMessageDialog(rootPane, "Successfully Added");
    }//GEN-LAST:event_btnAddActionPerformed

    private void tbDisplayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDisplayMouseClicked
        TableModel model = tbDisplay.getModel();
        int i = tbDisplay.getSelectedRow();
        txtId.setText(String.valueOf(model.getValueAt(i, 0)));
        txtName.setText(String.valueOf(model.getValueAt(i, 1)));
        if (String.valueOf(model.getValueAt(i, 2)).equals("Male")) {
            rMale.setSelected(true);
        }
        if (String.valueOf(model.getValueAt(i, 2)).equals("Female")) {
            rFemale.setSelected(true);
        }
        txtFname.setText(String.valueOf(model.getValueAt(i, 3)));
        cmbCls.setSelectedItem(model.getValueAt(i, 4));
        cmbSubject.setSelectedItem(model.getValueAt(i, 5));
        txtContract.setText(String.valueOf(model.getValueAt(i, 6)));
        txtAddress.setText(String.valueOf(model.getValueAt(i, 7)));

    }//GEN-LAST:event_tbDisplayMouseClicked

